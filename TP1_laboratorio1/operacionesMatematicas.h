/** \brief Funcion de adicion
 *
 * \param Primer entero
 * \param Segundo entero
 * \return La adicion de ambos enteros
 *
 */
int addition(int firstInteger, int secondInteger);

/** \brief Funcion de substraccion
 *
 * \param Primer entero
 * \param Segundo entero
 * \return La diferencia entre los enteros
 *
 */
int subtraction(int firstInteger, int secondInteger);

/** \brief Funcion de multiplicacion
 *
 * \param Primer entero
 * \param Segundo entero
 * \return El producto
 *
 */
int multiplication(int firstInteger, int secondInteger);

/** \brief Funcion de division
 *
 * \param Primer entero como dividendo
 * \param Segundo entero como divisor
 * \return El cociente, en caso de divisor ser igual a cero devuelve mensaje de error
 *
 */
int division(int firstInteger, int secondInteger);

/** \brief Funcion de factorizacion
 *
 * \param Un entero
 * \return El factorial de un entero
 *
 */
int factorial(int integer);
